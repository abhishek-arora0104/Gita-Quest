const contexts: PracticalExample["context"][] = [
  "School",
  "College",
  "Career",
  "Sports",
  "Relationships",
  "Social Media",
  "Daily Life",
];

function q(
  question: string,
  options: [string, string, string, string],
  correctIndex: 0 | 1 | 2 | 3,
  explanation: string,
): QuizSeed {
  return { question, options, correctIndex, explanation };
}

