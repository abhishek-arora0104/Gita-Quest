import type { Chapter, MainTeaching, PracticalExample, QuizQuestion } from "../schema";

type QuizSeed = Omit<QuizQuestion, "id" | "difficulty">;

type ChapterSeed = {
  number: number;
  slug: string;
  title: string;
  sanskritName: string;
  subtitle: string;
  readingTimeMins: number;
  wordCount: number;
  intro: string[];
  storyOverview: string[];
  mainTeachings: MainTeaching[];
  practicalExamples: PracticalExample[];
  lessonsForDailyLife: string[];
  keyTakeaways: string[];
  keyLessons: string[];
  reflectionQuestions: string[];
  seo: Chapter["seo"];
  quiz: {
    easy: QuizSeed[];
    medium: QuizSeed[];
    hard: QuizSeed[];
  };
};

type QuizPlan = {
  title: string;
  sanskritName: string;
  mainFocus: string;
  centralIdea: string;
  warning: string;
  encouragedAttitude: string;
  practice: string;
  remembrance: string;
  result: string;
  simpleSummary: string;
  application: string;
  commonMistake: string;
  deepLesson: string;
};

function makeChapter(seed: ChapterSeed): Chapter {
  return {
    ...seed,
    intro: seed.intro.join("\n\n"),
    storyOverview: seed.storyOverview.join("\n\n"),
    quiz: [
      ...seed.quiz.easy.map((q, index) => ({
        id: `ch${seed.number}-e${index + 1}`,
        difficulty: "easy" as const,
        ...q,
      })),
      ...seed.quiz.medium.map((q, index) => ({
        id: `ch${seed.number}-m${index + 1}`,
        difficulty: "medium" as const,
        ...q,
      })),
      ...seed.quiz.hard.map((q, index) => ({
        id: `ch${seed.number}-h${index + 1}`,
        difficulty: "hard" as const,
        ...q,
      })),
    ],
  };
}

function q(
  question: string,
  options: [string, string, string, string],
  correctIndex: 0 | 1 | 2 | 3,
  explanation: string,
): QuizSeed {
  return { question, options, correctIndex, explanation };
}

